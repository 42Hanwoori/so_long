#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	f;

	i = 0;
	f = (char)c;
	while (s[i])
		i++;
	if (c == 0)
		return ((char *)&s[i]);
	while (--i >= 0)
		if (f == s[i])
			return ((char *)&s[i]);
	return (0);
}
