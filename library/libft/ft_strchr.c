#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	f;
	int		i;

	f = (char)c;
	i = -1;
	while (s[++i])
		if (s[i] == f)
			return ((char *)&s[i]);
	if (f == 0)
		return ((char *)&s[i]);
	return (NULL);
}
